const translations = { he, en };

export type TranslationKey = keyof typeof he;

/** Use outside React (e.g. axios interceptors, Zustand) — follows `useAuthStore` language. */
export function translate(key: TranslationKey): string {
  const language = useAuthStore.getState().language;
  return translations[language][key];
}

/** API stores Angular i18n keys (e.g. NOTIFICATIONS.SOS_ALERT_TITLE) on notification rows. */
const NOTIFICATION_API_I18N_MAP: Record<string, TranslationKey> = {
  "NOTIFICATIONS.SOS_ALERT_TITLE": "notifSosAlertTitle",
  "NOTIFICATIONS.SOS_ALERT": "notifSosAlert",
  "NOTIFICATIONS.SOS_RESOLVED_TITLE": "notifSosResolvedTitle",
  "NOTIFICATIONS.SOS_RESOLVED": "notifSosResolved",
  "NOTIFICATIONS.PROVIDER_APPROVED_TITLE": "notifProviderApprovedTitle",
  "NOTIFICATIONS.PROVIDER_APPROVED": "notifProviderApproved",
  "NOTIFICATIONS.ACCOUNT_SUSPENDED_TITLE": "notifAccountSuspendedTitle",
  "NOTIFICATIONS.ACCOUNT_SUSPENDED": "notifAccountSuspended",
  "NOTIFICATIONS.PROVIDER_BANNED_TITLE": "notifProviderBannedTitle",
  "NOTIFICATIONS.PROVIDER_BANNED": "notifProviderBanned",
  "NOTIFICATIONS.ACCOUNT_REACTIVATED_TITLE": "notifAccountReactivatedTitle",
  "NOTIFICATIONS.ACCOUNT_REACTIVATED": "notifAccountReactivated",
  "NOTIFICATIONS.NEW": "notifNew",
};

export function resolveNotificationApiText(
  text: string | undefined | null,
  language: Language,
): string {
  const s = text?.trim() ?? "";
  if (!s) return "";
  const key = NOTIFICATION_API_I18N_MAP[s];
  if (key) return translations[language][key];
  return s;
}

export function useTranslation() {
  const language = useAuthStore((s) => s.language);
  const isHebrew = language === "he";

  return {
    t: (key: TranslationKey): string => translations[language][key],
    language,
    isHebrew,
    isRTL: isHebrew,
    /** Apply to Text with explicit start-aligned text (labels, headings). */
    rtlText: {
      textAlign: (isHebrew ? "right" : "left") as "right" | "left",
      writingDirection: (isHebrew ? "rtl" : "ltr") as "rtl" | "ltr",
    },
    /** Apply to Text that uses `text-center` — sets writingDirection only. */
    rtlStyle: {
      writingDirection: (isHebrew ? "rtl" : "ltr") as "rtl" | "ltr",
    },
    /** Apply to flex-row containers (input rows) so icons flip sides. */
    rtlRow: {
      flexDirection: (isHebrew ? "row-reverse" : "row") as
        | "row"
        | "row-reverse",
    },
    /** Apply to TextInput for correct cursor/text alignment. */
    rtlInput: {
      textAlign: (isHebrew ? "right" : "left") as "right" | "left",
      writingDirection: (isHebrew ? "rtl" : "ltr") as "rtl" | "ltr",
    },
    /** Tailwind class for label alignment. */
    alignCls: isHebrew ? "text-right" : "text-left",
  };
}
